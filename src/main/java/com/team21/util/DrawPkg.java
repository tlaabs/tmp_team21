package com.team21.util;

public class DrawPkg {
	int[] xs;
	int[] ys;
	
	public DrawPkg(int[] xs, int[] ys){
		this.xs = xs;
		this.ys = ys;
	}
	public int[] getXs() {
		return xs;
	}
	public void setXs(int[] xs) {
		this.xs = xs;
	}
	public int[] getYs() {
		return ys;
	}
	public void setYs(int[] ys) {
		this.ys = ys;
	}
	
	
}
